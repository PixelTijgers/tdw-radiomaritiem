    <h3 class="text-4xl mt-10 mb-5">Info</h3>

    <p>De haven van Antwerpen is de grootste zeehaven in België en de tweede haven in Europa, na Rotterdam. In 2014 had de haven van Antwerpen een overslag van ruim 199 miljoen ton. Voor stukgoed is Antwerpen een van de grootste havens van Europa, voor overslag van stortgoederen is ze minder belangrijk.</p>

    <p>De haven is verbonden met de Noordzee via de Westerschelde. De haven van Antwerpen speelt een belangrijke rol in de internationale handel. Ze is diep in het binnenland gelegen waardoor de af te leggen afstanden voor spoor- of wegvervoer opmerkelijk verminderen.</p>

    <p>Dit speelt in het voordeel van het containervervoer, dat veel gebruikmaakt van deze transportmogelijkheden. Het containervervoer is vanaf de jaren 50 sterk toegenomen, vooral door de toenmalige Waalse economie. Eenheidsladingen zijn nu eenmaal makkelijker te vervoeren en eenvoudiger te behandelen. Niet alleen de traditionele producten, zoals textiel en elektronica, worden per container vervoerd maar ook vruchten, luxeauto’s en andere nieuwe producten verschijnen op de containermarkt.</p>

